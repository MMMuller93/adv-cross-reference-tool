"""
Tests for the private-companies seed pipeline.

Covers the three deliverables required by the task brief:
1. Wikipedia loader returns 600+ rows including Anthropic / OpenAI / SpaceX / Stripe.
2. Merge correctly disambiguates Canva (Wikipedia) from CANVA AUSTRALIA HOLDINGS PTY LTD
   (N-PORT title), linking the latter via the manual alias table.
3. Wiz is flagged is_acquired=True with lifecycle_status='acquired'.

Plus a few correctness/sanity tests for normalization and alias rollup.

Network behavior
----------------
The Wikipedia loader test calls the live Wikipedia API. If `PFR_OFFLINE=1` is
set in the environment, those tests are skipped (e.g. for CI on an air-gapped
runner). All non-network tests run unconditionally.
"""
from __future__ import annotations

import json
import os
from pathlib import Path

import pytest

from nport.seed_loader import load_seed_supabase, merge_and_emit, wikipedia_loader

DATA_DIR = Path(__file__).resolve().parent.parent
OFFLINE = os.environ.get("PFR_OFFLINE") == "1"


# ----------------------------------------------------------------------------
# Wikipedia loader
# ----------------------------------------------------------------------------
@pytest.fixture(scope="module")
def wikipedia_entries():
    """Live call to the Wikipedia API — cached for the whole module."""
    if OFFLINE:
        pytest.skip("PFR_OFFLINE=1 — skipping live Wikipedia API test.")
    return wikipedia_loader.load_unicorns()


def test_wikipedia_returns_600_plus_rows(wikipedia_entries):
    """The plan §6.7 quotes 618 active + 206 exited; we tolerate ±50 row drift."""
    assert len(wikipedia_entries) >= 600, (
        f"Expected 600+ unicorn rows from Wikipedia; got {len(wikipedia_entries)}"
    )


@pytest.mark.parametrize("target", ["Anthropic", "OpenAI", "SpaceX", "Stripe"])
def test_wikipedia_contains_key_companies(wikipedia_entries, target):
    """Each named company must appear at least once in the parsed output."""
    hits = [e for e in wikipedia_entries if e.company.lower() == target.lower()]
    assert hits, f"Wikipedia loader missed '{target}'"
    e = hits[0]
    assert e.valuation_usd_billions and e.valuation_usd_billions > 0, (
        f"{target} has no valuation"
    )


def test_wikipedia_emits_active_and_exited_tables(wikipedia_entries):
    active = [e for e in wikipedia_entries if e.table == "active"]
    exited = [e for e in wikipedia_entries if e.table == "exited"]
    assert len(active) >= 500, f"too few active rows: {len(active)}"
    assert len(exited) >= 150, f"too few exited rows: {len(exited)}"
    # Uber should appear in exited (IPO 2019)
    uber = next((e for e in exited if e.company.lower() == "uber"), None)
    assert uber is not None, "Uber missing from exited table"


