# ----------------------------------------------------------------------------
# Merge pipeline
# ----------------------------------------------------------------------------
@pytest.fixture(scope="module")
def merged():
    """Run merge_and_emit against the seed JSONs in this dir.

    Wikipedia file may have been generated by an earlier test run / manual run.
    If absent and we're online, we generate it here.
    """
    wiki_path = DATA_DIR / "wikipedia_unicorns.json"
    if not wiki_path.exists():
        if OFFLINE:
            pytest.skip("Wikipedia seed missing and PFR_OFFLINE=1.")
        wikipedia_loader.main([])  # populates the default output path
    wikipedia = merge_and_emit.load_json(wiki_path)
    wikidata = merge_and_emit.load_json(DATA_DIR / "wikidata_enriched.json")
    nport = merge_and_emit.load_json(DATA_DIR / "nport_clusters.json")
    manual = merge_and_emit.load_json(DATA_DIR / "manual_curated_seed.json")
    lifecycle = merge_and_emit.load_json(DATA_DIR / "lifecycle_flags.json")
    companies = merge_and_emit.build_companies(wikipedia, wikidata, nport, manual, lifecycle)
    aliases = merge_and_emit.build_aliases(companies, manual)
    return companies, aliases


def test_merge_produces_canva_with_nport_alias(merged):
    """
    Canva is in the Wikipedia list as 'Canva' (Australia), but the N-PORT-discovered
    raw title is 'CANVA AUSTRALIA HOLDINGS PTY LTD'. The manual seed must encode
    an alias that lets the resolution pipeline match the N-PORT string to the
    Wikipedia entry's slug.
    """
    companies, aliases = merged
    assert "canva" in companies, "canva slug missing from merged companies"
    canva = companies["canva"]
    assert canva["display_name"] == "Canva", (
        f"Canva display_name unexpected: {canva['display_name']!r}"
    )

    # Aliases for canva
    canva_aliases = [a for a in aliases if a["company_slug"] == "canva"]
    assert canva_aliases, "no aliases for canva"

    # One alias must match the normalized form of the N-PORT raw title.
    nport_raw = "CANVA AUSTRALIA HOLDINGS PTY LTD"
    nport_norm = merge_and_emit.normalize_name(nport_raw)
    assert nport_norm == "CANVA AUSTRALIA", f"normalization changed: {nport_norm!r}"

    matching = [
        a for a in canva_aliases
        if a["pattern_type"] == "exact_normalized"
        and a["pattern"] == nport_norm
    ]
    assert matching, (
        "Expected an exact_normalized alias for 'CANVA AUSTRALIA' linking "
        "the N-PORT subsidiary title to the canva slug. Aliases: "
        f"{[a['pattern'] for a in canva_aliases]}"
    )


def test_wiz_is_acquired(merged):
    """Wiz must be flagged as acquired by Google."""
    companies, _ = merged
    assert "wiz" in companies, "wiz missing from merged companies"
    wiz = companies["wiz"]
    assert wiz["is_acquired"] is True, f"Wiz is_acquired wrong: {wiz['is_acquired']}"
    assert wiz["lifecycle_status"] == "acquired", (
        f"Wiz lifecycle_status wrong: {wiz['lifecycle_status']}"
    )
    assert "google" in (wiz["acquired_by"] or "").lower(), (
        f"Wiz acquired_by wrong: {wiz['acquired_by']}"
    )


def test_anthropic_has_full_alias_set(merged):
    """Manual seed should produce the documented alias rows.

    The task brief listed `ANTHROPIC PBC` as an exact_normalized example,
    but `PBC` is a legal-entity suffix that our normalize_name() strips, so
    by the time the alias hits the database both `ANTHROPIC` and
    `ANTHROPIC PBC` collapse to the same row — which is the correct
    behavior because the resolution pipeline will also normalize the
    incoming N-PORT string. We therefore check the post-normalization
    invariants instead of the pre-normalization examples.
    """
    _, aliases = merged
    ant = [a for a in aliases if a["company_slug"] == "anthropic"]
    patterns = {(a["pattern_type"], a["pattern"]) for a in ant}
    assert ("exact_normalized", "ANTHROPIC") in patterns
    assert ("prefix", "ANTHROPIC") in patterns
    assert any(
        a["pattern_type"] == "vendor_code"
        and a["vendor_code_type"] == "BlackRock"
        for a in ant
    ), "missing BlackRock vendor_code alias for Anthropic"

    # Sanity: the raw manual seed must still have included ANTHROPIC PBC
    # as a value the curator typed in (it just got normalized on emit).
    manual_path = DATA_DIR / "manual_curated_seed.json"
    raw = json.loads(manual_path.read_text())
    ant_entry = next(e for e in raw["entries"] if e["slug"] == "anthropic")
    raw_patterns = {a["pattern"] for a in ant_entry["aliases"]}
    assert "ANTHROPIC PBC" in raw_patterns, (
        "Curator-typed manual seed should retain 'ANTHROPIC PBC' verbatim"
    )


def test_aliases_have_required_fields(merged):
    """Every alias must have company_slug, pattern_type, pattern, exposure_type, source."""
    _, aliases = merged
    required = {"company_slug", "pattern_type", "pattern", "exposure_type", "source"}
    valid_pt = {"exact_normalized", "prefix", "contains", "regex", "vendor_code"}
    valid_et = {"direct", "spv", "feeder", "derivative", "credit"}
    for a in aliases:
        missing = required - set(a)
        assert not missing, f"alias missing fields {missing}: {a}"
        assert a["pattern_type"] in valid_pt, f"bad pattern_type: {a}"
        assert a["exposure_type"] in valid_et, f"bad exposure_type: {a}"
        assert a["pattern"].strip(), f"empty pattern: {a}"


def test_no_duplicate_aliases(merged):
    """No (slug, pattern_type, pattern) triple should appear twice."""
    _, aliases = merged
    seen = set()
    for a in aliases:
        key = (a["company_slug"], a["pattern_type"], a["pattern"])
        assert key not in seen, f"duplicate alias: {key}"
        seen.add(key)


